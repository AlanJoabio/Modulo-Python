'''
Operadores relacionais
'''

x = 5;
y = 7;

# maior que
print(x > y); # False, pois x é menor ou igual a y.
print();
# maior ou igual à (>=)
print (x >= y); # False, pois x é maior ou igual a y.
print();
# menor que (<)
print(x < y); # True, pois x é menor do que y.
print();
# menor ou igual à (<=) 
print(x <= y); # False
print();
#igualdade
print(x == y); #False
print();
#diferença entre os valores (!=)
print(x!= y); #True

